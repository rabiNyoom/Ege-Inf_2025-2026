'''
Для узла с IP-адресом 117.73.208.27 адрес сети равен 117.73.192.0. Каково наименьшее возможное количество нулей в разрядах маски?
'''

from ipaddress import *

for m in range(33):
    net = ip_network(f'117.73.208.27/{m}', strict=False)
    print(net, f'{net.netmask:b}'.count('0'))

# 117.73.192.0/18 14
# 117.73.192.0/19 (13) <--